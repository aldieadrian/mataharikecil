                <p>Berawal dari pemuda karang taruna komplek Gading Regensi yang diajak oleh pihak DKM Baiturrahman Gading Regensi , untuk melihat serta mencari solusi tentang kondisi angka putus sekolah disekitar yang ternyata masih ada. Hingga akhirnya di Agustus 2015 bentuklah sebuah sekolah bagi anak anak yang sebelumnya terancam putus sekolah, SMP Terbuka Firdaus kelas Gading Regensi. Jadi, sesuai namanya sekolah tersebut bisa terwujud berkat kerjasama dengan pemerintah yg membentuk program SMP Terbuka, serta SMPT Firdaus sendiri yang memang sudah sejak lama berdiri.</p>
